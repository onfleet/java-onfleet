package com.onfleet.models.worker;

public class WorkerAdditionalCapacities {
	private Double capacityA;
	private Double capacityB;
	private Double capacityC;

	public Double getCapacityA() {
		return capacityA;
	}

	public void setCapacityA(Double capacityA) {
		this.capacityA = capacityA;
	}

	public Double getCapacityB() {
		return capacityB;
	}

	public void setCapacityB(Double capacityB) {
		this.capacityB = capacityB;
	}

	public Double getCapacityC() {
		return capacityC;
	}

	public void setCapacityC(Double capacityC) {
		this.capacityC = capacityC;
	}
}
